// ! The SetInterval function receives two parameters, where 
// ! the first corresponds to the callback (function) and the 
// ! second is the amount of time expressed in milliseconds in 
// ! which the interval to be applied is to be carried out or applied.
SetInterval(() => {
    Console.Log('Hello world');
}, 1000);