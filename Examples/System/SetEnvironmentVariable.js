// The following method allows you to add an environment variable 
// to the set that the operating system already has, it receives as 
// the first parameter the name that you want to assign to the environment 
// variable and as the second parameter the value that it will have, in this 
// example we will assign an environment variable which will contain the 
// name of the creator of this technology.
System.SetEnvironmentVariable('ZENDA_AUTHOR_NAME', 'Rodolfo Herrera Hernandez');